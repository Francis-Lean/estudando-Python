algo = input('digite algo: ')
print('o tipo primitivo desse valor é:', type(algo))
print('esse valor é alfanumérico?', algo.isalnum())
print('esse valor é alfabético?', algo.isalpha())
print('esse valor é numérico?', algo.isnumeric())
print('esse valor é formado apenas por números?', algo.isalnum())
print('esse valor é composto apenas por letras maiúsculas?', algo.isupper())
print('esse valor é composto apenas por letras minúsculas?', algo.islower())
print('esse valor é um dígito?', algo.isdigit())
print('esse valor é formado apenas por espaços?', algo.isspace())
print('esse valor é um número decimal?', algo.isdecimal())



